#!/usr/bin/env python3

nbr1=int("10")
nbr2=float("5")


# dodawanie
result = nbr1 + nbr2
print(result)
print(str(nbr1)+ "+" +str(nbr2)+ "=" +str(result)+ str(type(result)))

# odejmowanie 
result = nbr1 - nbr2
print(result)
print(str(nbr1)+ "-" + str(nbr2)+ "=" +str(result) + str(type(result)))

# mnozenie
result = nbr1 * nbr2
print(result)
print(str(nbr1)+ "*" +str(nbr2)+ "=" +str(result)+ str(type(result)))

# dzielenie
result = nbr1 / nbr2
print(result)
print(str(nbr1)+ " /" +str(nbr2)+ "=" +str(result)+ str(type(result)))

# dzielnie modulo
result = nbr1 % nbr2
print(result)
print( str(nbr1)+ " %" +str(nbr2)+ "=" +str(result)+ str(type(result)))

# dzielnie calkowite
result = nbr1 // nbr2
print(result)
print( str(nbr1)+ "//" +str(nbr2)+ "=" +str(result)+ str(type(result)))

# Potegowanie
result = nbr1 ** nbr2
print(result)
print( str(nbr1)+ "**" +str(nbr2)+ "=" +str(result)+ str(type(result)))

